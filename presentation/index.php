                        <h2 class="h3 page-title mt-n2">Qu'est-ce que le PRASMESTI ?</h2>
                        <p class="mb-35"> Construit autour d’objectifs normatifs communs, le PRASMESTI est le portail mis en place pour parvenir à une intégration régionale réussie. Il repose d’abord naturellement sur le développement d’échanges intracommunautaires et veille à ce que la diversité de tous et de chacun en matière d’éducation, formation, sciences, technologie et innovation s’exprime en florilège dans un dialogue formalisé et dans un langage clair.
                            Il ne s’agit jamais d’uniformiser les structures, les pratiques et les projets. Il s’agit d’harmoniser la diversité des pratiques, d’exploiter au niveau régional les résultats des programmes nationaux pour établir des conditions de dialogues clairs et cohérents !
                            <br> En termes de pratiques, le PRASMESTI a pour mission de :
                        </p>
                        <div class="checklist style2">
                            <ul>
                                <li>partager les informations sur la vie du monde de l’éducation, de la formation, des sciences, de la technologie et de l’innovation au sein de notre espace économique régional pour répondre aux défis qui s’imposent à nous et identifier de nouveaux horizons, etc. ;</li>
                                <li>rendre disponibles les normes et standards de qualité de l’éducation, de la formation, des sciences, de la technologie et de l’innovation au sein de notre Communauté Économique Régionale ;</li>
                                <li>donner du sens aux innovations en le rendant accessibles et orienter de manière stratégique les efforts et les missions de tous et de chacun en matière d’éducation, de formation, de sciences, de technologie et d’innovation ;</li>
                                <li>valoriser la culture d’éducation-formation, sciences, technologie et innovation en Afrique centrale par le renforcement, selon les orientations de l’Agenda 2063, de notre sentiment d’appartenance à une seule et même entité riche de sa pluralité culturelle et éducative ;</li>
                                <li>valoriser et développer les potentiels et talents de nos systèmes éducatifs ;</li>
                                <li>permettre à chaque État membre de partager ses réussites, ou tout simplement ses challenges en matière d’éducation et de formation : formation initiale, développement de la petite enfance, formatien en alternance, formation continue, formation tout au long de la vie, innovation, sciences, technologie, innovation, etc.</li>
                            </ul>
                        </div>
                        <br />

                        <p class="mb-45">Mise en place d'aide au suivi de la mise en oeuvre des textes normatifs en éducation est une démarche dont la réussite dépend de la façon dont il sera inséré dans la vie de la Communauté ainsi que des informations partagées : contenu, les interactions et, pourquoi pas, une ligne éditoriale, etc.</p>
                        <h3 class="mb-15">Description :</h3>
                        <p class="mb-45">Ainsi le PRASMESTI, qui se destine à être le trait d’union intra-communautaire en Afrique Centrale, répond à un double objectif : (1) accélérer l’intégration régionale par l’éducation, la formation, les sciences, la technologie et l’innovation en optimisant l’accès à l’information et aux données fiables ; et (2) s’adapter aux habitudes de lecture et aux usages de communication modernes par une assistance numérique apportée à chaque État membre de la Communauté et de la Commission de la CEEAC à faire le point des avancées régionales en matière d’intégration académique, de sciences, de technologie et d’innovation.</p>
                        <h3 class="mb-15">Le PRASMESTI : Pourquoi ?</h3>
                        <div class="checklist style2">
                            <ul>
                                <li>capitaliser les informations et les savoir-faire en éducation, formation, sciences, technologie et innovation dans l’espace CEEAC ;</li>
                                <li>simplifier la recherche d’informations sectorielles dans la région Afrique centrale ;</li>
                                <li>centraliser l’ensemble des données en éducation, sciences, technologie et innovation en un seul accès ;</li>
                                <li>se fédérer autour de la mise en oeuvre des textes normatifs et l’exécution des programmes régionaux.</li>
                            </ul>
