                            L'organe d'exécution de la Communauté au lieu de la Communauté (Commission) choisit, selon le profil de chaque utilisateur, de mettre à la disposition de tel ou telle usager les informations ou les applications de son système. Ci-dessous les principaux types d’accès :
                            <ul class="listeElement16L1">
